# Tipo de dadoo bool (boolean)
# Ao questionar algo em um programa, só existem duas responstar possíveis:
#Sim (True) ou Não (False).
# Existem vários operadores para "questionar".
# Dentre eles o ==, que é um operador lógico
# questiona se o valor é igual a outro

print (10 == 10) # Sim -> True (Verdadeiro)
print (11 == 10) # Não -> False (Falso)
print (type(11 == 10)) # Vai retorna o tipo do operador no caso bool
print(type(10 == 10))