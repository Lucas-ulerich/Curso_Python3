# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# positivo ou negativo. Int sem sinal é considerado positivo.

print(11) # int
print(-11) # int
print(0) # int

# float -> Número com ponto flutuante
# O tipo float representa qualquer número positivo ou negativo
# com pontu flutuante.
# float sem snal é considerato positivo.

print(1.1, 10.11) #float
print(0.0, -1.5)

# função type mostra o tipo que o python inferiu o valor.

print(type('Lucas'))
print(type(3))
print(type(-1.2))
print(type(0.9))